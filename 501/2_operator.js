//Addition
var a = 5 + 5;
console.log(a);
console.log(typeof a); //number

//Subtraction
var b = 5 - 5;
console.log(b);
console.log(typeof b); //number

//Multiplication
var c = 5 * 5;
console.log(c);
console.log(typeof c); //number

//Division
var d = 5 / 5;
console.log(d);
console.log(typeof d); //number

var e = (5 + 5) * 5;
console.log(e);

var f = 3 ** 2;
console.log(f);
