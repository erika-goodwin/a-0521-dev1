// Strings
var str = "Mary is an Instructor";
console.log(str);
console.log(typeof str); //string

// Numbers
var num = 27.22;
console.log(num);
console.log(typeof num); //number

// Booleans
var num1 = 5;
var num2 = 10;
console.log(num1 < num2); //true

var dt = num1 < num2;
console.log(dt); //true
console.log(typeof dt); //boolean
console.log("typeof dt:", typeof dt);

//Undefined
var a;
console.log(a); //undefined
console.log(typeof a); //undefined

//Null
var b = null;
console.log(b); //null
console.log(typeof b); //Object
